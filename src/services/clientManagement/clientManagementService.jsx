export function saveClient(client) {
  let clientInDb = clients.find(m => m._id === client._id) || {};
  clientInDb.name = client.name;
  clientInDb.numberInStock = client.numberInStock;
  clientInDb.dailyRentalRate = client.dailyRentalRate;

  if (!clientInDb._id) {
    clientInDb._id = Date.now();
    clients.push(clientInDb);
  }

  return clientInDb;
}

